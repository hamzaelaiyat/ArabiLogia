  static String _toArabicIndic(int number) {
    const arabic = '٠١٢٣٤٥٦٧٨٩';
    return number.toString().split('').map((c) => arabic[c.codeUnitAt(0) - 48]).join();
  }

  static String _randomNumber() {
    final random = Random();
    final n = 2 + random.nextInt(998);
    final num = _toArabicIndic(n);
    final stars = '⭐' * (random.nextInt(3) + 1);
    return '$stars $num $stars';
  }

  static Future<String> generate({required SupabaseClient supabase}) async {
    final random = Random();
    while (true) {
      final animal = _animals[random.nextInt(_animals.length)];
      final feeling = _feelings[random.nextInt(_feelings.length)];
      final number = _randomNumber();
      final name = '$animal $feeling $number';

      try {
        final exists = await supabase.rpc('check_random_name_exists', params: {'name': name});
        if (exists == false) return name;
      } catch (e) {
        rethrow;
      }
    }
  }
